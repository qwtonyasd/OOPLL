struct Vec2Comparator {
    bool operator()(const glm::vec2& a, const glm::vec2& b) const {
        if (std::abs(a.x - b.x) > 1.0f) return a.x < b.x;
        if (std::abs(a.y - b.y) > 1.0f) return a.y < b.y;
        return false;
    }
};

